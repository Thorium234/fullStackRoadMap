// Using let (recommended for variables that will change)
let age = 25;

// Using const (for values that won't change)
const name = "John";

// Using var (older way, generally avoided in modern JavaScript)
var isStudent = true;
// Number
let count = 42;
let price = 19.99;

// String
let greeting = "Hello";
let message = 'World';

// Boolean
let isActive = true;
let isComplete = false;

// Undefined
let result;  // Value is undefined

// Null
let data = null;

// Object
let person = {
    firstName: "Jane",
    lastName: "Doe",
    age: 28
};

// Array (a type of object)
let colors = ["red", "green", "blue"];
